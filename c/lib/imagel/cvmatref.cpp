#include "imagel_.h"

cvMatRef :: cvMatRef ( void )
	{
	////////////////////////////////////////////////////////////////////////
	//
	//	PURPOSE
	//		-	Constructor for the object
	//
	////////////////////////////////////////////////////////////////////////
	mat		= NULL;
	gpumat	= NULL;
	umat		= NULL;

	// So creators do not have to do the initial AddRef
	AddRef();
	}	// cvMatRef

cvMatRef :: ~cvMatRef ( void )
	{
	////////////////////////////////////////////////////////////////////////
	//
	//	PURPOSE
	//		-	Destructor for the object
	//
	////////////////////////////////////////////////////////////////////////
	if (mat != NULL)
		{
		delete mat;
		mat = NULL;
		}	 // if
	if (gpumat != NULL)
		{
		delete gpumat;
		gpumat = NULL;
		}	 // if
	if (umat != NULL)
		{
		delete umat;
		umat = NULL;
		}	 // if
	}	// ~cvMatRef

