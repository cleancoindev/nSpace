#include "imagel_.h"

cvMatRef :: cvMatRef ( void )
	{
	////////////////////////////////////////////////////////////////////////
	//
	//	PURPOSE
	//		-	Constructor for the object
	//
	////////////////////////////////////////////////////////////////////////
	mat = NULL;

	// So creators do not have to do the initial AddRef
	AddRef();
	}	// cvMatRef

void cvMatRef :: destruct ( void )
	{
	////////////////////////////////////////////////////////////////////////
	//
	//	OVERLOAD
	//	FROM		CCLObject
	//
	//	PURPOSE
	//		-	Called when the object is being destroyed
	//
	////////////////////////////////////////////////////////////////////////
	if (mat != NULL)
		{
		delete mat;
		mat = NULL;
		}	 // if
	}	// destruct
